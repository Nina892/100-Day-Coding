public class Day029 {
    public static void main(String[] args) {
        double number1 = 5.5;
        double number2 = 2.0;
        
        // Melakukan operasi aritmatika non-primitif dengan tipe data double
        double tambah = number1 + number2;
        double kurang= number1 - number2;
        double kali = number1 * number2;
        double bagi = number1 / number2;
        
        // Menampilkan hasil operasi
        System.out.println("Penjumlahan: " + tambah);
        System.out.println("Pengurangan: " + kurang);
        System.out.println("Perkalian: " + kali);
        System.out.println("Pembagian: " + bagi);
    }
}
