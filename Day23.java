public class NonPrimitifExample {
    public static void main(String[] args) {
        // Tipe data non-primitif: String
        String nama = "Nina";

        // Tipe data non-primitif: Integer
        Integer umur = 21;

        // Tipe data non-primitif: Double
        double jajan = 200.0;

        // Tipe data non-primitif: Boolean
        Boolean sudahMenikah = false;

        System.out.println("Nama: " + nama);
        System.out.println("Umur: " + umur);
        System.out.println("jajan: " +  jajan);
        System.out.println("Sudah Menikah: " + sudahMenikah);
    }
}
